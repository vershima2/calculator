x=parseFloat(parseInt(prompt("Enter first number")));
y=parseFloat(parseInt(prompt("Enter second number")));
oper=prompt("Type an operator");

if (oper== '+'){
z = x+y;
alert("sum:  " +z);
} 
else if (oper=='-'){
    a = x-y
    alert("Difference:  " +a);
}
else if(oper=='*'){
    b = x*y
    alert("Product:  " +b);
}
else if(oper=='/'){
    c = x/y
    alert("quotient:  " +c);
}





